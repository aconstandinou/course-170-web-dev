- Although it is not something youd normally do, it is possible to interact
  with HTTP manually because it is a text-based protocol.
- HTTP is built on top of TCP, which is a networking layer that handles
  communicating between two computers.
- URLs are made up of many components: a protocol, a host, a port, a path,
  and parameters.
- Query parameters are parameters that are included in a URL. They are appended
  to the path using ?. Parameters are specified in the URL using the form key=value.
- Parameters after the first are appended to the URL using &.
- HTTP is stateless, which means that each request is handled separately by the
  server. By carefully crafting URLs and parameters, stateful interactions can
  be built on top of HTTP.
