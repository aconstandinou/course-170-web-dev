# URL
https://amazon.com/Double-Stainless-Commercial-Refrigerator/B60HON32?ie=UTF8&qid=142952676&sr=93&keywords=commercial+fridge

1. a) Identify Host
      amazon.com
1. b) Identify names of query parameters
      ie, qid, sr, keywords
1. c) Identify values of query parameters
      UTF8, 142952676, 93, commercial+fridge
1. d) Identify protocol
      https
1. e) Identify path
      /Double-Stainless-Commercial-Refrigerator/B60HON32
1. f) Identify port
      This URL does not contain a port. Most software will use port 443 by default
      when working with this URL due to it having a protocol of https, but that
      information is not contained within the URL.

2. Add port 3000 to the URL
http://amazon.com":3000"/products/B60HON32?qid=142952676&sr=93

3. Given the URL:
http://localhost:4567/todos/15
3. a) Identify query parameters
      None
   b) Identify protocol
      http
   c) Identify path
      /todos/15
   d) Identify host
      localhost
   e) Identify port
      4567

4. What are two different ways to encode a space in a query parameter? We
   didnt cover this in the lectures, so feel free to search online for the answer.
   1) the plus sign "+"
   2) the %20 encoded

5. What character indicates the beginning of a URLs query parameters?
   the question mark "?"

6. What character is used between the name and value of a query parameter?
   the equal sign "="

7. What character is used between multiple query parameters?
   the ampersand sign "&"
